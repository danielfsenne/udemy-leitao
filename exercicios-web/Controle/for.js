let contador = 1 
while(contador  <= 10) {
    console.log(`contador = ${contador}`)
    contador++
}
for(let i = 1; i <= 10; i++) { // Divisão do for consiste na declaração da variavel, a expressão e o incremento
    console.log(`i = ${i}`)
}

const notas = [6.7, 7.7, 9.8, 8.1, 7.4]
